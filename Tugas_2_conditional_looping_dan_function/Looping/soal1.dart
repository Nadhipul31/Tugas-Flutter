void main() {
  print("Looping Pertama");
  for (var deret = 2; deret <= 20; deret += 2) {
    print(deret.toString() + ' - I Love Coding');
  }
  print("----------------------------");

  print("Looping Kedua");
  for (var deret = 20; deret >= 2; deret -= 2) {
    print(deret.toString() + ' - I will become a mobile developer');
  }
}
